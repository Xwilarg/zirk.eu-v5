function getJamHtml(jam, idCmpl) {
    // Display buttons under the jam preview
    let buttons = '<span class="button-container">';

    // Links where the user can play the jam (WebGL)
    for (let webgl in jam.webgl) {
        let sourceText = "";
        if (jam.webgl.length > 1) {
            sourceText = " Source " + (parseInt(webgl) + 1);
        }
        if (jam.nsfw) {
            buttons += '<a class="button disabled">Play (WebGL' + sourceText + ')</a>';
        } else {
            buttons += '<a class="button colorful" href="' + jam.webgl[webgl] + '">Play (WebGL' + sourceText + ')</a>';
        }
    }
    buttons += '</span>';
    if (!jam.nsfw) { // Only display source and website if the jam is SFW
        buttons += '<span class="button-container">';
        if (jam.github !== null) {
            buttons += '<a class="button neutral" href="' + jam.github + '">Source Code</a>';
        }
        if (jam.website !== null) {
            buttons += '<a class="button neutral" href="' + jam.website + '">Jam Website</a>';
        }
        buttons += "</span>"
    }

    let jamInfo = "";
    if (jam.rating !== null && jam.rating.scores != null && jam.rating.scores["Overall"].rank != null) {
        const overall = jam.rating.scores["Overall"].rank;
        const entries = jam.rating.entries;
        jamInfo = `Ranked ${overall} out of ${entries}<br/>(${(overall / entries * 100).toFixed(1)}% tier)`;
    }

    return `
        <span id="jam-${jam.name}${idCmpl}">
            <span class="jamInfoText">
                <p class="jamTitle">${jam.event} - ${jam.duration} hour${jam.duration > 1 ? "s" : ""}<br/>${(jam.theme.length > 0 ? jam.theme[0] : "")}</p>
                <p class="jamInfo">${jamInfo}</p>
            </span>
            <img id="jamimg-${jam.name}${idCmpl}" src="img/gamejam/${jam.name}.jpg"></img>
            <br/>
            ${buttons}
        </span>
    `;
}

function jamMouseOver(id, name) {
    let img = document.getElementById(id);
    // https://stackoverflow.com/questions/5775469/whats-the-valid-way-to-include-an-image-with-no-src#comment27184376_14115340
    img.src = "data:image/gif;base64,R0lGODlhAQABAIAAAP///wAAACH5BAEAAAAALAAAAAABAAEAAAICRAEAOw==";
    img.style.backgroundImage = "url(img/gamejam/" + name + ".gif)";
}

function jamMouseOut(id, name) {
    let img = document.getElementById(id);
    img.src = "img/gamejam/" + name + ".jpg";
    img.style.backgroundImage = "";
}

function drawSpeJam(jam, inId, outId) {
    document.getElementById(outId).innerHTML = getJamHtml(jam, inId);
    let bestSpan = document.getElementById("jam-" + jam.name + inId);
    bestSpan.addEventListener("mouseover", () => { jamMouseOver("jamimg-" + jam.name + inId, jam.name); }, false);
    bestSpan.addEventListener("mouseout", () => { jamMouseOut("jamimg-" + jam.name + inId, jam.name); }, false);
}

// Wait for window to load so jamDisplay isn't null
function initGamejam() {
    let jamDisplay = document.getElementById("jamDisplay");
    
    let html = "";
    // Display all jams
    for (let index in gamejams)
    {
        let jam = gamejams[index];

        html += getJamHtml(jam, "");
    }
    jamDisplay.innerHTML = html;

    // Add mouse over events
    for (let index in gamejams)
    {
        let jam = gamejams[index];

        let span = document.getElementById("jam-" + jam.name);
        span.addEventListener("mouseover", () => { jamMouseOver("jamimg-" + jam.name, jam.name); }, false);
        span.addEventListener("mouseout", () => { jamMouseOut("jamimg-" + jam.name, jam.name); }, false);
    }
    drawSpeJam(
        gamejams
        .filter(x => x.fullName === "anomaly.exe")[0]
    , "favorite", "jamFavorite");
    drawSpeJam(
        gamejams
        .filter(x => x.rating !== null && x.rating.scores != null && x.rating.scores["Overall"].rank != null)
        .sort((a, b) => {
            return (a.rating.scores["Overall"].rank / a.rating.entries) - (b.rating.scores["Overall"].rank / b.rating.entries);
        })[0]
    , "best", "jamBestRanked");
    drawSpeJam(
        gamejams
        .filter(x => x.engine !== "Unity")[0]
    , "nonUnity", "jamLatestNonUnity");
    for (let jam in gamejams
        .slice(0, 3))
    {
        drawSpeJam(
            gamejams[jam]
        , "latest" + jam, "jamLatest" + jam);
    }
}